// Создание экземпляра таймера
const timer = new Timer();

// Установка времени (в секундах)
timer.setTimer(300);

// Запуск таймера
timer.start();

// Определение действия по истечении времени
timer.onTimerEnd(() => {
    console.log("Время вышло!");
});

